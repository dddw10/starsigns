/**
 * 获取伪随机数发生器
 */
function getSeededRandom(seedStr) {
  let hash = 0;
  for (let i = 0; i < seedStr.length; i++) {
    hash = seedStr.charCodeAt(i) + ((hash << 5) - hash);
  }
  return () => {
    const x = Math.sin(hash++) * 10000;
    return x - Math.floor(x);
  };
}

/**
 * 姓名配对主算法
 */
function nameMatchAnalysis(data) {
  const { name1, name2, relationType = 'love' } = data;

  const strokes1 = calculateStrokes(name1);
  const strokes2 = calculateStrokes(name2);

  const strokeSum1 = strokes1.reduce((a, b) => a + b, 0);
  const strokeSum2 = strokes2.reduce((a, b) => a + b, 0);

  const wuxing1 = getWuxingByStrokes(strokeSum1);
  const wuxing2 = getWuxingByStrokes(strokeSum2);

  const relation = WUXING_RELATIONS[wuxing1][wuxing2];

  // 基础分值计算
  let score = 65;

  // 五行交互积分
  if (relation === 'generate_to' || relation === 'generate_by') {
    score += 20; 
  } else if (relation === 'same') {
    score += 15; 
  } else if (relation === 'control_by' || relation === 'control_to') {
    score -= 12; 
  }

  // 笔画奇偶配合 (阴阳调和)
  const isOdd1 = strokeSum1 % 2 !== 0;
  const isOdd2 = strokeSum2 % 2 !== 0;
  if (isOdd1 !== isOdd2) {
    score += 8; 
  } else {
    score += 3;
  }

  // 动态数理微调，保证不同笔画在同五行生克下分数不一致
  score += (strokeSum1 + strokeSum2) % 7;

  // 限制最终范围为 40 - 99 分
  const finalScore = Math.min(99, Math.max(40, score));

  // 星级判定
  let rating = 3;
  if (finalScore >= 90) rating = 5;
  else if (finalScore >= 80) rating = 4;
  else if (finalScore >= 65) rating = 3;
  else if (finalScore >= 50) rating = 2;
  else rating = 1;

  // 生成配对分析文案
  const analysis = generateMatchReport(name1, name2, strokes1, strokes2, strokeSum1, strokeSum2, wuxing1, wuxing2, relation, relationType, finalScore);

  return {
    name1,
    name2,
    strokeSum1,
    strokeSum2,
    wuxing1,
    wuxing2,
    relationType,
    score: finalScore,
    rating,
    analysis
  };
}

